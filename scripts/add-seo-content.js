// ── Обход файлов ──────────────────────────────────────────────────────────────

let count = 0;

for (const [keyPart, locData] of Object.entries(LOCATIONS)) {
  const filePath = path.join(SERVICE, 'zamena', keyPart + '.html');

  if (!fs.existsSync(filePath)) {
    console.warn(`  ⚠ Файл не найден: ${keyPart}.html`);
    continue;
  }

  const src = fs.readFileSync(filePath, 'utf8');

  // Не трогаем если уже есть seo-text
  if (src.includes('seo-text')) {
    console.log(`  — уже есть seo-text: ${keyPart}`);
    continue;
  }

  const seoBlock = buildSeoText(keyPart, locData);

  // Вставляем перед <section class="contact"
  const anchor = '<section class="contact"';
  if (!src.includes(anchor)) {
    console.warn(`  ⚠ Не найдена секция contact: ${keyPart}`);
    continue;
  }

  const newSrc = src.replace(anchor, `${seoBlock}\n\n    ${anchor}`);
  fs.writeFileSync(filePath, newSrc, 'utf8');
  count++;
  console.log(`  ✓ ${keyPart}`);
}

console.log(`\n✅ SEO-блок добавлен на ${count} страниц`);
